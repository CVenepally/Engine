#include "Engine/Math/LineSegment3.hpp"
#include "Engine/Core/EngineCommon.hpp"
//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
LineSegment3::LineSegment3()
{}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
LineSegment3::~LineSegment3()
{}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
LineSegment3::LineSegment3(Vec3 start, Vec3 end)
	:m_start(start)
{

	m_fwdNormal = (end - start).GetNormalized();

	m_length = (end - start).GetLength();

}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
LineSegment3::LineSegment3(Vec3 start, Vec3 fwdNormal, float length)
	: m_start(start)
	, m_fwdNormal(fwdNormal)
	, m_length(length)
{

}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vec3 LineSegment3::GetCenter() const
{
	return m_start + m_fwdNormal * m_length * 0.5f;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
void LineSegment3::TranslateLineSegment(Vec3 translation)
{
	UNUSED(translation);
}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
void LineSegment3::TranslateEndPoint(Vec3 translation)
{
	UNUSED(translation);
}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
void LineSegment3::TranslateStartPoint(Vec3 translation)
{
	UNUSED(translation);
}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
void LineSegment3::SetCenter(Vec3 newCenter)
{
	m_start = newCenter - m_fwdNormal * m_length * 0.5f;
}


//-------------------------------------------------------------------------------------------------------------------------------------------------------------------
void LineSegment3::RotateAboutCenter(float rotationDeltaDegrees)
{
	UNUSED(rotationDeltaDegrees);
}
